<template>
  <div class="navbar">
    <hamburger :is-active="sidebar.opened" class="hamburger-container" @toggleClick="toggleSideBar" />

    <breadcrumb class="breadcrumb-container" />

    <div class="right-menu">
      <!-- <BaseSearch /> -->
      <div class="welcome">欢迎你，{{ userInfo.displayName }}</div>
      <el-dropdown class="avatar-container" trigger="click">
        <div class="avatar-wrapper">
          <img :src="avatar+'?imageView2/1/w/80/h/80'" class="user-avatar">
          <i class="el-icon-caret-bottom" />
        </div>
        <el-dropdown-menu slot="dropdown" class="user-dropdown">
          <router-link to="/">
            <el-dropdown-item>
              首页
            </el-dropdown-item>
          </router-link>
          <el-dropdown-item @click.native="openEditDialog">
            修改个人信息
          </el-dropdown-item>
          <el-dropdown-item @click.native="openEditPwdDialog">
            修改登录密码
          </el-dropdown-item>
          <el-dropdown-item divided @click.native="logout">
            <span style="display:block;">退出登录</span>
          </el-dropdown-item>
        </el-dropdown-menu>
      </el-dropdown>
    </div>

    <EditForm ref="editForm" />
    <EditPwdForm ref="editPwdForm" />
  </div>
</template>

<script>
import { mapGetters } from 'vuex'
import Breadcrumb from '@/components/Breadcrumb'
import Hamburger from '@/components/Hamburger'
import EditForm from '@/views/user/edit'
import EditPwdForm from '@/views/user/editPwd'
export default {
  components: {
    Breadcrumb,
    Hamburger,
    EditForm,
    EditPwdForm
  },

  computed: {
    ...mapGetters([
      'sidebar',
      'avatar',
      'userInfo'
    ])
  },
  methods: {
    toggleSideBar() {
      this.$store.dispatch('app/toggleSideBar')
    },
    openEditDialog() {
      this.$refs.editForm.show('editUserInfo', this.userInfo)
    },
    openEditPwdDialog() {
      this.$refs.editPwdForm.show(this.userInfo.id)
    },
    async logout() {
      await this.$store.dispatch('user/logout')
      this.$router.push(`/login?redirect=${this.$route.fullPath}`)
    }
  }
}
</script>

